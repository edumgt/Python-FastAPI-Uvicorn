# 파일 상단 구분선을 통해 예제 시작 지점을 시각적으로 구분한다.
# -----------------------------
# 현재 파일이 Python 리스트 CRUD 예제임을 설명한다.
# ListCrudExample in Python
# 파일 상단 구분선을 닫아 헤더 영역을 마무리한다.
# -----------------------------

# 리스트 CRUD 동작을 순서대로 시연하는 메인 함수를 정의한다.
def main():
    # 어떤 타입이든 저장 가능한 빈 리스트를 생성한다.
    fruits = []

    # Create 섹션 시작을 구분선으로 표시한다.
    # -----------------------------
    # C(Create): 요소를 생성/추가하는 단계임을 설명한다.
    # C: Create (생성/추가)
    # Create 섹션 구분선을 닫는다.
    # -----------------------------
    # "Apple" 문자열을 리스트 끝에 추가한다.
    fruits.append("Apple")
    # "Orange" 문자열을 리스트 끝에 추가한다.
    fruits.append("Orange")

    # Read 섹션 시작을 구분선으로 표시한다.
    # -----------------------------
    # R(Read): 요소를 조회하는 단계임을 설명한다.
    # R: Read (조회)
    # Read 섹션 구분선을 닫는다.
    # -----------------------------
    # 인덱스 1의 값을 읽어 첫 번째 출력 예시를 보여준다.
    print("First fruit:", fruits[1])

    # 리스트의 모든 요소를 순회하기 위한 반복문을 시작한다.
    for fruit_name in fruits:
        # 현재 요소 문자열 자체를 출력한다.
        print("Fruit:", fruit_name)
        # 현재 요소 문자열 길이를 계산해 출력한다.
        print("Length:", len(fruit_name))

    # Update 섹션 시작을 구분선으로 표시한다.
    # -----------------------------
    # U(Update): 기존 요소를 수정하는 단계임을 설명한다.
    # U: Update (수정)
    # Update 섹션 구분선을 닫는다.
    # -----------------------------
    # 인덱스 1 값을 "Orange"에서 "Banana"로 변경한다.
    fruits[1] = "Banana"
    # 수정된 인덱스 1 값을 출력해 변경 결과를 확인한다.
    print(fruits[1])

    # Delete 섹션 시작을 구분선으로 표시한다.
    # -----------------------------
    # D(Delete): 요소를 삭제하는 단계임을 설명한다.
    # D: Delete (삭제)
    # Delete 섹션 구분선을 닫는다.
    # -----------------------------
    # 값이 "Apple"인 요소를 리스트에서 제거한다.
    fruits.remove("Apple")
    # 리스트의 남은 요소를 전부 삭제해 빈 리스트로 만든다.
    fruits.clear()

# 현재 파일이 직접 실행될 때만 main()을 호출한다.
if __name__ == "__main__":
    # CRUD 데모를 실행하기 위해 main 함수를 호출한다.
    main()
